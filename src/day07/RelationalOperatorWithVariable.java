package day07;

public class RelationalOperatorWithVariable {
    public static void main(String[] args) {

        int mynum = 10;
        System.out.println(mynum>5);
        System.out.println(mynum< 19);
        System.out.println(mynum >= 5);
        System.out.println(mynum<= 55);

        System.out.println(mynum == 10);
        System.out.println(mynum != 9);
    }
}
