package day03;

public class PrintVsPrintln {
    public static void main(String[] args) {

        System.out.println("hello\tworld");
        System.out.println("hello\nworld");
        System.out.println("hello\'world");
        System.out.println("hello\"world");
        System.out.println("hello\\world");
        System.out.println("I like the book \"Java\"");
        System.out.println("the movie name is \'the lord of the rings\'");
        System.out.println("hello\tworld");
        System.out.println("hello\nbatch15");
        System.out.print("this is a regular line \n");
        System.out.println("this is println");
        System.out.print("i want to print the next sentence in the next line\n");

    }

}
