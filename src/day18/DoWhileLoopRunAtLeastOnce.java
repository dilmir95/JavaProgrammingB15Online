package day18;

public class DoWhileLoopRunAtLeastOnce {

    public static void main(String[] args) {

        int x = 1000;
        do{
            System.out.println("keep going");
        }while(x<5);

        System.out.println("the end");


        while (x<5){
            System.out.println("KEEP GOING IN WHILE LOOP");
        }
    }
}
