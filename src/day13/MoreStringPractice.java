package day13;

public class MoreStringPractice {

    public static void main(String[] args) {

        String name = "Dilmurod";
        System.out.println(name);
        name = "Panda";
        System.out.println(name);
        name.toUpperCase();
        System.out.println(name);

        System.out.println(name.toUpperCase());

        System.out.println(name);

        name = name.toUpperCase();
        System.out.println(name);
    }
}
